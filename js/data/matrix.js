// Function to convert from gunasthan-based to thana-based structure
function convertToThanaBasedStructure(gunasthanBasedData) {
    const thanaBasedData = { 'default': {} };
    
    // Initialize structure for all 24 thanas
    for (let thanaIndex = 0; thanaIndex < 24; thanaIndex++) {
        thanaBasedData['default'][thanaIndex] = {};
    }
    
    // Convert the data
    for (let gunasthanId = 1; gunasthanId <= 14; gunasthanId++) {
        const gunasthanData = gunasthanBasedData[gunasthanId];
        
        if (!gunasthanData) continue;
        
        for (let thanaIndex = 0; thanaIndex < 24; thanaIndex++) {
            const thanaData = gunasthanData[thanaIndex];
            
            if (thanaData) {
                thanaBasedData['default'][thanaIndex][gunasthanId] = thanaData;
            }
        }
    }
    
    return thanaBasedData;
}

